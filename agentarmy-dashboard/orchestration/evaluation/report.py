"""
Report Generator — produces JSON and Markdown evaluation reports.

Outputs:
- evaluation_results.json — raw results + aggregates
- evaluation_report.md — human-readable Markdown summary
"""

from __future__ import annotations

import json
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Dict, List

from .engine import EvaluationResult


def generate_json_report(
    results: List[EvaluationResult],
    aggregates: Dict[str, Any],
    output_path: str | Path,
) -> Path:
    """Write full evaluation results as JSON."""
    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    report = {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "summary": aggregates,
        "results": [r.to_dict() for r in results],
    }

    output_path.write_text(json.dumps(report, indent=2), encoding="utf-8")
    return output_path


def generate_markdown_report(
    results: List[EvaluationResult],
    aggregates: Dict[str, Any],
    output_path: str | Path,
) -> Path:
    """Write a human-readable Markdown evaluation report."""
    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    lines: List[str] = []
    _a = lines.append

    _a("# AgentArmy Evaluation Report")
    _a(f"\n**Generated:** {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M:%S UTC')}\n")

    # Overall Summary
    _a("## Overall Summary\n")
    _a(f"| Metric | Value |")
    _a(f"|--------|-------|")
    _a(f"| Total Cases | {aggregates.get('total_cases', 0)} |")
    _a(f"| Pass Rate | {aggregates.get('overall_pass_rate', 0):.1%} |")
    _a(f"| Mean Score | {aggregates.get('overall_mean_score', 0):.4f} |")

    # Per-Agent Breakdown
    _a("\n## Per-Agent Results\n")
    by_agent = aggregates.get("by_agent", {})
    if by_agent:
        _a("| Agent | Cases | Passed | Pass Rate | Mean | Min | Max |")
        _a("|-------|-------|--------|-----------|------|-----|-----|")
        for agent_type, stats in sorted(by_agent.items()):
            _a(
                f"| {agent_type.capitalize()} "
                f"| {stats['cases']} "
                f"| {stats['passed']} "
                f"| {stats['pass_rate']:.1%} "
                f"| {stats['mean_score']:.4f} "
                f"| {stats['min_score']:.4f} "
                f"| {stats['max_score']:.4f} |"
            )

    # Per-Evaluator Breakdown
    _a("\n## Per-Evaluator Results\n")
    by_evaluator = aggregates.get("by_evaluator", {})
    if by_evaluator:
        _a("| Evaluator | Count | Pass Rate | Mean Score |")
        _a("|-----------|-------|-----------|------------|")
        for name, stats in sorted(by_evaluator.items()):
            _a(
                f"| {name} "
                f"| {stats['count']} "
                f"| {stats['pass_rate']:.1%} "
                f"| {stats['mean_score']:.4f} |"
            )

    # Detailed Per-Case Results
    _a("\n## Detailed Results\n")
    for r in results:
        status = "PASS" if r.passed else "FAIL"
        icon = "✅" if r.passed else "❌"
        _a(f"### {icon} {r.case_id} ({r.agent_type}) — {status} ({r.aggregate_score:.4f})\n")

        for ev in r.evaluations:
            ev_status = "✅" if ev.get("passed") else "❌"
            _a(f"- {ev_status} **{ev.get('evaluator', '?')}**: {ev.get('score', 0):.4f}")

            # Show key details
            details = ev.get("details", {})
            if details:
                # Show missing fields if any
                missing = details.get("missing_fields", [])
                if missing:
                    _a(f"  - Missing: {', '.join(missing)}")
                # Show issues
                issues = details.get("governor_issues", [])
                for iss in issues:
                    _a(f"  - ⚠️ {iss}")
                # Show marker leaks
                leaks = details.get("sensitive_marker_leaks", [])
                for leak in leaks:
                    _a(f"  - 🔐 Leaked: `{leak['marker']}` ({leak['matches']} occurrences)")

            if ev.get("error"):
                _a(f"  - ⚠️ Error: {ev['error']}")

        _a("")  # blank line

    # Footer
    _a("---")
    _a("*Report generated by AgentArmy Evaluation Framework*")

    output_path.write_text("\n".join(lines), encoding="utf-8")
    return output_path
